package com.snail.webgame.engine.component.scene.common;


public class BattlePoint
{
	protected int side;
	protected String type;
	protected int x;
	protected int y;
	protected String hinderList;
	protected int x1;
	protected int y1;
	public int getSide() {
		return side;
	}
	public void setSide(int side) {
		this.side = side;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public String getHinderList() {
		return hinderList;
	}
	public void setHinderList(String hinderList) {
		this.hinderList = hinderList;
	}
	public int getX1() {
		return x1;
	}
	public void setX1(int x1) {
		this.x1 = x1;
	}
	public int getY1() {
		return y1;
	}
	public void setY1(int y1) {
		this.y1 = y1;
	}


}
