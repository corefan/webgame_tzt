package com.snail.webgame.unite.ui.info;

/**
 * 组件
 * @author panxj
 * @version 1.0 2010-7-22
 */

public class CompCfgInfo {
	private String name;
	private String type;
	private String text;
	private int winX;
	private int winY;
	private int width;
	private int height;
	private int visible;	
	
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getType()
	{
		return type;
	}
	public void setType(String type)
	{
		this.type = type;
	}
	public String getText()
	{
		return text;
	}
	public void setText(String text)
	{
		this.text = text;
	}
	public int getWinX()
	{
		return winX;
	}
	public void setWinX(int winX)
	{
		this.winX = winX;
	}
	public int getWinY()
	{
		return winY;
	}
	public void setWinY(int winY)
	{
		this.winY = winY;
	}
	public int getWidth()
	{
		return width;
	}
	public void setWidth(int width)
	{
		this.width = width;
	}
	public int getHeight()
	{
		return height;
	}
	public void setHeight(int height)
	{
		this.height = height;
	}
	public int getVisible()
	{
		return visible;
	}
	public void setVisible(int visible)
	{
		this.visible = visible;
	}	
}
