package com.snail.webgame.unite.ui.info;

/**
 * 组件信息
 * @author panxj
 * @version 1.0 2010-8-17
 */

public class CompUnitInfo {
	
	private String compName;
	private String addSite;
	private String compType;
	private Object compInfo;
	private String reserve;
	private String locat;
	
	public String getCompName()
	{
		return compName;
	}
	public void setCompName(String compName)
	{
		this.compName = compName;
	}
	public String getAddSite()
	{
		return addSite;
	}
	public void setAddSite(String addSite)
	{
		this.addSite = addSite;
	}
	public String getCompType()
	{
		return compType;
	}
	public void setCompType(String compType)
	{
		this.compType = compType;
	}
	public Object getCompInfo()
	{
		return compInfo;
	}
	public void setCompInfo(Object compInfo)
	{
		this.compInfo = compInfo;
	}
	public String getReserve()
	{
		return reserve;
	}
	public void setReserve(String reserve)
	{
		this.reserve = reserve;
	}
	public String getLocat()
	{
		return locat;
	}
	public void setLocat(String locat)
	{
		this.locat = locat;
	}
}
